document.getElementById('Div1').addEventListener('click', function () {
    let minhaDiv = document.getElementById('drown');

    if (minhaDiv.style.display === 'none') {
        minhaDiv.style.display = 'block';
    }
    else {
        minhaDiv.style.display = 'none';
    }
});

